# Number Manipulation (Zahlen manipulation)

gewicht = 10

print(gewicht)          # Ergebnis = 10

gewicht_neu = gewicht + 2
print(gewicht_neu)      # Ergebnis = 12

####################################
#Additionszuweisungg (Langform x = x + 1) -> Kurzform x += 1

gewicht += 2            # Gewicht wird um 2 erhöht, das gleiche wie gewicht = gewicht + 2
print(gewicht)          # Ergebnis = 14

# Zuweisung
#  ... bedeutet ein Wert in einer Variable gespeichert

# Operator
# ... ist ein Zeichen (+|-|\*|%|**), das etwas mit Werten macht. Sie führen Aktionen (Berechnungen oder Vergleiche) aus.

# Arithmetischer Operator
# ... ist ein Operator, der eine mathematische Berechnung durchführt. Es gibt 5 arithmetische Operatoren in Python: +, -, *, /, % und **

# Zusammengesetze Operator
# ... mehrere Dinge werden kombiniert. Bsp.: gewicht += 2 (wird gerechnet und gespeichert)

##################################################

# Subtraktionszuweiseung (Langform x = x - 1) -> Kurzform x -= 1
leckerlis = 20
leckerlis -= 4            # Leckerlis werden um 4 reduziert, das gleiche wie leckerlis = leckerlis - 4
print (leckerlis)         # Ergebnis = 16

##################################################

# Multiplikationszuweisung (Langform x = x * 2) -> Kurzform x *= 2
futter = 5
futter *= 7              # Futter wird mit 7 multipliziert, das gleiche wie futter = futter * 7
print(futter)            # Ergebnis = 35

#############################################################################

# Divisioonszuweisung (Langform x = x / 2) -> Kurzform x /= 2
futter = 36
futter /= 3
print(futter)            # Ergebnis = 12.0, weil Division immer eine Kommazahl zurückgibt (auch wenn das Ergebnis eine ganze Zahl ist)